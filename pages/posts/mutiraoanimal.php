            <h2 class="mb-4">A Amparo juntamente com a Prefeitura realizam mutirão inédito de saúde animal em Ouricuri</h2>
            <div>
                <span>14 de Setembro de 2025</span>
            </div>
            <p class="text-justify">
                Em um esforço conjunto para promover o bem-estar animal, a Associação Mantenedora de Proteção aos Animais de Ouricuri, em parceria com a Prefeitura Municipal, realizou um mutirão inédito de
                saúde animal. O evento, que aconteceu no último fim de semana (14/09), ofereceu uma série de serviços
                veterinários gratuitos para cães e gatos da comunidade.
            </p>
            <p class="text-justify">
                No último domingo (14/09/2025), o Bairro Residencial Maria Gorete, em Casinhas – Ouricuri, recebeu uma ação inédita dedicada ao cuidado de cães e gatos. A iniciativa foi fruto de uma parceria entre a Prefeitura Municipal e a Amparo – Associação Mantenedora de Proteção aos Animais de Ouricuri, reunindo moradores e tutores em um mutirão de serviços veterinários que trouxe grandes benefícios para a comunidade.
            </p>
            <p class="text-justify">
                O evento contou com a colaboração voluntária de médicos veterinários e profissionais da área de saúde, que atenderam os animais durante todo o dia. Entre os serviços oferecidos estiveram consultas, exames de imagem (como ultrassonografia), testes de leishmaniose e calazar, além da aplicação de vacinas antirrábicas e anti-cio.
            </p>
            <p class="text-justify">
                O Castra Móvel também marcou presença, funcionando como estrutura de apoio laboratorial. Embora não tenham sido realizadas castrações nesta edição, o equipamento deu suporte técnico ao mutirão e reforçou a qualidade dos atendimentos prestados.
            </p>
            <p class="text-justify">
                Um dos pontos mais importantes da ação foi a vacinação em massa contra a raiva, contemplando todos os animais assistidos e fortalecendo a prevenção de uma das principais doenças transmissíveis da região.
            </p>
            <p class="text-justify">
                A realização só foi possível graças ao esforço conjunto da Prefeitura de Ouricuri, da Amparo e do apoio da Secretaria de Saúde, Diretoria de Meio Ambiente e Vigilância Sanitária.
            </p>
            <p class="text-justify">
                Com o êxito do mutirão, o município dá mais um passo significativo em prol do bem-estar animal e da saúde pública. A previsão é que a iniciativa seja levada a outras comunidades de Ouricuri, ampliando cada vez mais o alcance das ações preventivas e de cuidado.
            </p>
            <p class="text-justify">
                Não podemos nos esquecer dos Patrocinadores e apoiadores que tornaram este evento possível, reforçando o compromisso coletivo com a causa animal. A Amparo agradece a todos que contribuíram para o sucesso desta ação e reafirma seu compromisso contínuo com a proteção e o cuidado dos animais de Ouricuri. Juntos, podemos fazer a diferença!
            </p>
            <p>
                Os moradores do bairro e tutores de animais expressaram sua gratidão pela oportunidade de cuidar de seus pets sem custos, destacando a importância de iniciativas como essa para a comunidade local. A iniciativa de poder contar conosco é muito importante para todos nós.
            </p>
